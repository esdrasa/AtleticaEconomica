    private void buscarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarButtonActionPerformed
        // TODO add your handling code here:
        String str = JOptionPane.showInputDialog("Digite a matricula do aluno:");
        if(str != null){
            long matricula = Long.parseLong(str);
            Aluno aluno;

            try {
                aluno = banco.buscar(matricula);

                if(aluno != null){
                    new TelaBusca(banco, aluno, banco.buscarPosicao(matricula)).setVisible(true);
                    this.setVisible(false);
                }else{
                    JOptionPane.showMessageDialog(this.getContentPane(), "Aluno não encontrado");
                }
            } catch (IOException ex) {
                Logger.getLogger(TelaInicial.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_buscarButtonActionPerformed

    private void removerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removerButtonActionPerformed
        // TODO add your handling code here:
        long matricula = Long.parseLong(JOptionPane.showInputDialog("Digite a matricula:"));
        
        try {
            if(banco.remover(matricula)){
                JOptionPane.showMessageDialog(this.getContentPane(), "Aluno removido");
            }else{
                JOptionPane.showMessageDialog(this.getContentPane(), "Aluno não está no banco de dados");
            }
        } catch (IOException ex) {
            Logger.getLogger(TelaInicial.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_removerButtonActionPerformed

    private void gerarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_gerarButtonActionPerformed
        // TODO add your handling code here:
        String str = JOptionPane.showInputDialog("Digite a matricula do aluno:");
        if(str != null){
            long matricula = Long.parseLong(str);
            Aluno aluno;

            try {
                aluno = banco.buscar(matricula);

                if(aluno != null){
                    new AtleticaMembershipCard().generateID(aluno);
                    JOptionPane.showMessageDialog(this.getContentPane(), "Carteirinha salva");
                }else{
                    JOptionPane.showMessageDialog(this.getContentPane(), "Aluno não encontrado");
                }
            } catch (IOException ex) {
                Logger.getLogger(TelaInicial.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_gerarButtonActionPerformed

    private void gerarAllButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_gerarAllButtonActionPerformed
        try {
            // TODO add your handling code here:
            ArrayList<Aluno> alunos = banco.getAlunos();
            AtleticaMembershipCard gerador = new AtleticaMembershipCard();
            for(Aluno aluno : alunos){
                gerador.generateID(aluno);
            }
            JOptionPane.showMessageDialog(this.getContentPane(), "Carteirinhas geradas");
        } catch (IOException ex) {
            Logger.getLogger(TelaInicial.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_gerarAllButtonActionPerformed

